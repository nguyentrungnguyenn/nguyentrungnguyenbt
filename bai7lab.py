import math
r = int(input("Nhập bán kính hình tròn:"))
Chu_vi = round(2*math.pi*r, 2)
Dien_tich = round(math.pi*r**2, 2)
print("Chu vi hình tròn là:", Chu_vi)
print("Diện tích hình tròn là:", Dien_tich)


